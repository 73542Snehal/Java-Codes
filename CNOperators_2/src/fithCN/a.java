package fithCN;
//increment and decrement operator

public class a {

	public static void main(String[] args) {
		
		int a = 50;
		int b= 90;
		int c,d;
		
		c = a++;//post increment
		System.out.println("c="+c+ " a="+a);
		
		d = ++b;//pre increment
		System.out.println("d="+d+ " b="+b);
		
		int A = 10;
		int B= 20;
		int C,D;
		
		C = A--;//post increment
		System.out.println("C="+C+ " A="+A);
		
		D = --B;//pre increment
		System.out.println("D="+D+ " B="+B);
		
		
		

	}

}
