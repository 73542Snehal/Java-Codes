package fithCN;
//bitwise operator

public class c {
	public static void main(String[] args) {
		int a=10,b=2;
		
		System.out.println(a&b);//AND 1010 & 0010
		System.out.println(a|b);//OR 1010 | 0010
		System.out.println(a^b);//XOR 1010 ^ 0010
		System.out.println(~a);//NOT ~1010 
		System.out.println(~b);//~ 0010
		
		System.out.println(9^9);
		
		
	}

}
